%% 小行星运行轨道计算问题
% 由开普勒第一定律知，小行星运行轨道为椭圆方程: a1x^2+2a2xy+a3y^2a4x+2a5y+1=0
x=[1.02,0.87,0.67,0.44,0.16];
y=[0.39,0.27,0.18,0.13,0.13];
A = zeros(length(x)); % 初始化系数矩阵
for i=1:length(x)
    A(i,:)=[x(i)^2,2*x(i)*y(i),y(i)^2,2*x(i),2*y(i)];
end
b = -ones(length(x),1);
ai = A\b;
f = @(x,y) 2.4645*x.^2-0.8846*x.*y+6.4917*y.^2-1.3638*x-7.2016*y+1;
% ezplot是画出隐函数图形，是形如f(x,y)=0这种(画圆锥曲线比较适用)不能写出像y=f(x)这种函数的图形,
ezplot(f,[-0.5,1.2,0,1.2])